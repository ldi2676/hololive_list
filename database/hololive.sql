DROP DATABASE IF EXISTS hololivedb;

CREATE DATABASE IF NOT EXISTS hololivedb 
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;

USE hololivedb;
  
  CREATE TABLE hololive_company (
  id INT NOT NULL AUTO_INCREMENT, #값이 빈채로 놔두면 안된다는 의미
  name VARCHAR(255) NOT NULL,
  company VARCHAR(100) NOT NULL,
  division VARCHAR(100) NOT NULL,
  team VARCHAR(100) NOT NULL,
   
  PRIMARY KEY (id)
) ENGINE = InnoDB
  DEFAULT CHARACTER SET utf8 
  DEFAULT COLLATE utf8_general_ci;
  
    CREATE TABLE hololive_member (
    id INT NOT NULL AUTO_INCREMENT, #값이 빈채로 놔두면 안된다는 의미
    name VARCHAR(255) NOT NULL, 
    generation VARCHAR(100) NOT NULL,
    remark VARCHAR(100),
    PRIMARY KEY (id)
)   ENGINE = InnoDB
    DEFAULT CHARACTER SET utf8 
    DEFAULT COLLATE utf8_general_ci;
    
    CREATE TABLE hololive_member_image (
    id INT NOT NULL AUTO_INCREMENT, #값이 빈채로 놔두면 안된다는 의미
    name VARCHAR(255) NOT NULL, 
    image_url VARCHAR(100),
    PRIMARY KEY (id)
)   ENGINE = InnoDB
    DEFAULT CHARACTER SET utf8 
    DEFAULT COLLATE utf8_general_ci;

